# ----- Compute macro-average Precision-Recall -----
def compute_macro_pr(y_true, y_score):
    precision_dict = {}
    recall_dict = {}
    ap_dict = {}
    for i in range(n_classes):
        precision_dict[i], recall_dict[i], _ = precision_recall_curve(y_true[:, i], y_score[:, i])
        ap_dict[i] = average_precision_score(y_true[:, i], y_score[:, i])
    # Create a common recall grid
    all_recalls = np.linspace(0, 1, 100)
    mean_precision = np.zeros_like(all_recalls)
    for i in range(n_classes):
        mean_precision += np.interp(all_recalls, recall_dict[i][::-1], precision_dict[i][::-1])
    mean_precision /= n_classes
    macro_ap = np.mean(list(ap_dict.values()))
    return all_recalls, mean_precision, macro_ap

recall_cnn, precision_cnn, ap_cnn = compute_macro_pr(y_true, y_score_cnn)
recall_mobilenet, precision_mobilenet, ap_mobilenet = compute_macro_pr(y_true, y_score_mobilenet)
recall_hybrid, precision_hybrid, ap_hybrid = compute_macro_pr(y_true, y_score_hybrid)

# ----- Plot -----
plt.figure(figsize=(8,6))
plt.plot(recall_cnn, precision_cnn, linestyle='--', lw=2,
         label=f'NormalCNN1d (AP = {ap_cnn:.3f})', color='blue')
