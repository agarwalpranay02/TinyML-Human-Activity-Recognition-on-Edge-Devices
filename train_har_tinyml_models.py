# 3) Helper to load signals
def load_signals(data_root, subset='train'):
    signal_names = [
      'body_acc_x','body_acc_y','body_acc_z',
      'body_gyro_x','body_gyro_y','body_gyro_z',
      'total_acc_x','total_acc_y','total_acc_z'
    ]
    signals = []
    for s in signal_names:
        fname = os.path.join(data_root, subset, 'Inertial Signals', f"{s}_{subset}.txt")
        if not os.path.exists(fname):
            raise FileNotFoundError(f"Missing file: {fname}")
        arr = np.loadtxt(fname)          # shape (n_samples, 128)
        signals.append(arr)
    stacked = np.array(signals).transpose(1,2,0)  # (n_samples, 128, 9)
    return stacked

def load_labels(data_root, subset='train'):
    y_path = os.path.join(data_root, subset, f"y_{subset}.txt")
    y = np.loadtxt(y_path).astype(int) - 1   # convert 1..6 -> 0..5
    return y

