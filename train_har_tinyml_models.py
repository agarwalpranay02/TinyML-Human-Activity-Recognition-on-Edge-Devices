# ----- Compute macro-average ROC for each model -----
def compute_macro_roc(y_true, y_score):
    fpr_dict = {}
    tpr_dict = {}
    roc_auc_dict = {}
    for i in range(n_classes):
        fpr_dict[i], tpr_dict[i], _ = roc_curve(y_true[:, i], y_score[:, i])
        roc_auc_dict[i] = auc(fpr_dict[i], tpr_dict[i])
    # Interpolate all ROC curves to a common grid
    all_fpr = np.unique(np.concatenate([fpr_dict[i] for i in range(n_classes)]))
    mean_tpr = np.zeros_like(all_fpr)
    for i in range(n_classes):
        mean_tpr += np.interp(all_fpr, fpr_dict[i], tpr_dict[i])
    mean_tpr /= n_classes
    return all_fpr, mean_tpr, auc(all_fpr, mean_tpr)

fpr_cnn, tpr_cnn, roc_auc_cnn = compute_macro_roc(y_true, y_score_cnn)
fpr_mobilenet, tpr_mobilenet, roc_auc_mobilenet = compute_macro_roc(y_true, y_score_mobilenet)
fpr_hybrid, tpr_hybrid, roc_auc_hybrid = compute_macro_roc(y_true, y_score_hybrid)

