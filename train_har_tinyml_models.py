assert 'X_train' in globals() and 'X_test' in globals(), "Run dataset-loading cells first."
assert 'y_train_cat' in globals() and 'y_test_cat' in globals(), "Run Cell 33 to create label files."

input_shape = X_train.shape[1:]   # (128,9)
num_classes = y_train_cat.shape[1]

def create_cnn_bilstm(input_shape, num_classes):
    inp = Input(shape=input_shape)

    # Conv block 1
    x = Conv1D(64, kernel_size=3, padding='same', activation='relu')(inp)
    x = BatchNormalization()(x)
    x = MaxPooling1D(pool_size=2)(x)
    x = Dropout(0.25)(x)

    # Conv block 2
    x = Conv1D(128, kernel_size=3, padding='same', activation='relu')(x)
    x = BatchNormalization()(x)
    x = MaxPooling1D(pool_size=2)(x)
    x = Dropout(0.25)(x)

    # Option: global pooling or flatten before LSTM. We'll keep timesteps -> LSTM:
    # After two poolings, time dimension reduced from 128 -> 32 (approx), with feature channels = 128
    # Bidirectional LSTM expects (timesteps, features)
    x = Bidirectional(LSTM(64, return_sequences=False))(x)  # returns vector
    x = Dropout(0.3)(x)

    x = Dense(128, activation='relu')(x)
    x = Dropout(0.4)(x)
    out = Dense(num_classes, activation='softmax')(x)

    model = Model(inp, out)
    return model

# Build model
